import assert from "node:assert/strict";
import { readFileSync } from "node:fs";

import {
  IINA_DEFAULT_INPUT_CONF,
  activeKeyMappingsLastWins,
  generateInputConf,
  keyMappingConflictState,
  keyMappingPreferenceRows,
  normalizeKeyMapping,
  normalizeMpvKey,
  parseInputConf,
  removeShadowedKeyMappings,
  serializeKeyMapping,
} from "../src/key-mapping.js";
import {
  isModifierOnlyKeyboardEvent,
  macOSModifierSymbols,
  macOSReadableKey,
  macOSReadableKeyboardEvent,
  macOSReadableMpvKey,
  macOSReadableSavedFilterShortcut,
  mpvKeyTokenFromKeyboardEvent,
} from "../src/key-display.js";

const normalizationCases = new Map([
  ["Shift+-", "_"],
  ["Shift+=", "PLUS"],
  ["Meta+Alt+X", "Alt+Meta+X"],
  ["meta+shift+k", "Meta+K"],
  ["esc", "ESC"],
  ["CTRL+SHIFT+SHIFT+SHIFT+x", "Ctrl+X"],
  ["#", "SHARP"],
  ["+", "PLUS"],
  ["default-bindings", "default-bindings"],
  ["-------", "-------"],
]);

for (const [rawKey, expected] of normalizationCases) {
  assert.equal(normalizeMpvKey(rawKey), expected, `normalizeMpvKey(${JSON.stringify(rawKey)})`);
}

const parsedDefaults = parseInputConf(IINA_DEFAULT_INPUT_CONF);
const referenceDefaultInputConf = readFileSync(
  new URL("../参考/iina/iina/config/iina-default-input.conf", import.meta.url),
  "utf8",
).replace(/\n$/, "");
assert.equal(IINA_DEFAULT_INPUT_CONF, referenceDefaultInputConf, "the embedded profile must match IINA 1.3.5 apart from its terminal newline");
assert.equal(parsedDefaults.length, 83, "the embedded IINA 1.3.5 default profile should keep every mapping");
assert.equal(parsedDefaults[0].rawKey, "Shift+Meta+v");
assert.equal(parsedDefaults[0].rawAction, "video-panel");
assert.equal(parsedDefaults[0].isIINACommand, true);

const parsed = parseInputConf(`# ordinary comment
#@iina Meta+Alt+x toggle-pip   #  keep # tail
Shift+-   {default} cycle   pause  # note
q {video} quit
default-bindings start
  #@iina Meta+y toggle-music-mode
`);

assert.equal(parsed.length, 4);
assert.deepEqual(
  {
    rawKey: parsed[0].rawKey,
    rawAction: parsed[0].rawAction,
    comment: parsed[0].comment,
    isIINACommand: parsed[0].isIINACommand,
  },
  {
    rawKey: "Meta+Alt+x",
    rawAction: "toggle-pip",
    comment: "  keep # tail",
    isIINACommand: true,
  },
);
assert.equal(parsed[1].rawKey, "Shift+-", "parsing must preserve rawKey fidelity");
assert.equal(parsed[1].rawAction, "{default} cycle   pause");
assert.equal(parsed[1].effectiveRawAction, "cycle pause");
assert.equal(parsed[1].runtimeEligible, true);
assert.equal(parsed[2].runtimeEligible, false);
assert.equal(parsed[2].inactiveReason, "section:video");
assert.equal(parsed[3].runtimeEligible, false);
assert.equal(parsed[3].inactiveReason, "default-bindings");

const generated = generateInputConf(parsed);
assert.ok(generated.startsWith("# Generated by IINA\n\n"));
assert.ok(generated.includes("#@iina Meta+Alt+x toggle-pip   #  keep # tail\n"));
assert.ok(generated.includes("Shift+- {default} cycle pause   # note\n"));
const roundTripped = parseInputConf(generated);
assert.equal(roundTripped[0].rawKey, parsed[0].rawKey);
assert.equal(roundTripped[0].comment, parsed[0].comment);
assert.equal(roundTripped[1].rawKey, parsed[1].rawKey);
assert.equal(roundTripped[1].normalizedRawAction, parsed[1].normalizedRawAction);

const conflictRows = parseInputConf(`Meta+Alt+X cycle pause
Alt+Meta+X {default} cycle mute
Alt+Meta+X {video} stop
`);
const active = activeKeyMappingsLastWins(conflictRows);
assert.equal(active.length, 1);
assert.equal(active[0].rawAction, "{default} cycle mute", "the last eligible normalized key must win");

const conflicts = keyMappingConflictState(conflictRows);
assert.deepEqual([...conflicts.duplicateSignatures], ["Alt+Meta+X"]);
assert.deepEqual([...conflicts.shadowedIndexes], [0]);
assert.deepEqual([...conflicts.activeIndexes], [1]);
assert.deepEqual(
  removeShadowedKeyMappings(conflictRows).map((row) => row.rawAction),
  ["{default} cycle mute", "{video} stop"],
);

const generic = normalizeKeyMapping({ rawKey: "SPACE", rawAction: "cycle   pause" });
assert.deepEqual(generic.action, { type: "mpv-command", action: "cycle   pause" });
assert.deepEqual(serializeKeyMapping(generic), {
  id: "binding-0",
  rawKey: "SPACE",
  rawAction: "cycle   pause",
  comment: undefined,
  isIINACommand: false,
});
assert.deepEqual(activeKeyMappingsLastWins([]), []);
const preferenceDefaults = [{ rawKey: "SPACE", rawAction: "cycle pause" }];
assert.deepEqual(keyMappingPreferenceRows(null, preferenceDefaults), preferenceDefaults);
assert.deepEqual(keyMappingPreferenceRows(undefined, preferenceDefaults), preferenceDefaults);
assert.deepEqual(keyMappingPreferenceRows([], preferenceDefaults), []);

assert.equal(macOSReadableKey("SPACE"), "␣");
assert.equal(macOSReadableKey("LEFT"), "←");
assert.equal(macOSReadableKey("INS"), "Ins");
assert.equal(macOSReadableKey("KP_DEC"), ".");
assert.equal(macOSReadableKey("f13"), "F13");
assert.equal(macOSReadableKey("PLAYPAUSE"), "▶︎ ❙ ❙");
assert.equal(macOSModifierSymbols({ ctrl: true, alt: true, shift: true, meta: true }, "x"), "⌃⌥⇧⌘");
assert.equal(macOSModifierSymbols({ meta: true }, "K"), "⇧⌘");
assert.equal(macOSReadableMpvKey("RIGHT", { alt: true }), "⌥→");
assert.equal(macOSReadableMpvKey("SPACE", { meta: true }), "⌘␣");
assert.equal(macOSReadableSavedFilterShortcut("ö", "om"), "⌥⌘Ö");

const keyboardCases = [
  [{ key: " ", code: "Space" }, "SPACE", "␣"],
  [{ key: "ArrowLeft", code: "ArrowLeft", altKey: true }, "LEFT", "⌥←"],
  [{ key: "F19", code: "F19", shiftKey: true }, "F19", "⇧F19"],
  [{ key: "1", code: "Numpad1" }, "KP1", "1"],
  [{ key: ".", code: "NumpadDecimal" }, "KP_DEC", "."],
  [{ key: "+", code: "NumpadAdd" }, "PLUS", "+"],
  [{ key: "MediaTrackNext", code: "MediaTrackNext" }, "NEXT", "▶︎▶︎"],
  [{ key: "ö", code: "Semicolon" }, ";", "Ö"],
  [{ key: "Ö", code: "Semicolon", shiftKey: true }, ";", "⇧Ö"],
];
for (const [event, token, display] of keyboardCases) {
  assert.equal(mpvKeyTokenFromKeyboardEvent(event), token, `mpv token for ${event.key}`);
  assert.equal(macOSReadableKeyboardEvent(event), display, `macOS display for ${event.key}`);
}
assert.equal(isModifierOnlyKeyboardEvent({ key: "Meta" }), true);
assert.equal(isModifierOnlyKeyboardEvent({ key: "CapsLock" }), true);
assert.equal(isModifierOnlyKeyboardEvent({ key: "Enter" }), false);

const frontendSource = readFileSync(new URL("../src/main.js", import.meta.url), "utf8");
assert.match(frontendSource, /function pluginMpvKeyFromEvent\(event\) \{\s*return mpvKeyTokenFromKeyboardEvent\(event\);/u);
assert.match(frontendSource, /function eventKeyToken\(event\) \{\s*return mpvKeyTokenFromKeyboardEvent\(event\);/u);
assert.match(frontendSource, /els\.filterEditorShortcut\.value = macOSReadableKeyboardEvent\(event\);/u);

console.log("key-mapping tests passed");
